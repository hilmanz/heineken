                                <h3 class="title-entry">Sentiment Analysis</h3>
                                <div class="entry-content">
                                    <p>Sonar uses the NLP (Natural Language Processing) concept to determine the authors' tone and &quot;feeling&quot; in short posts, such as twitter and Facebook posts, and extended posts such as media articles and forum threads. Utilizing 3 years and over 2TBs of language data collection, Sonar trains itself to predict and analyze sentiment values of certain content. The way we process social media and formal media also differs, as semantics and lexicons used in both styles are extremely different.&nbsp;<strong> </strong></p>
                                    <p>For instance, to analyze twitter posts one must take into account &quot;feelings&quot; usually in the form of emoticons, punctuations and other spoken language icons unique to twitter. We also take into account the &quot;distance&quot; a sentiment tagged word is to it's entity. Logically, the more closer a descriptor to it's entity, the more direct the speaker wishes to make a point. Well, you get what we mean. &nbsp;<strong> </strong></p>
                                    <p>Currently we've tagged over 100,000 words, phases and entities in Bahasa Indonesia over the last 3 years, enabling Sonar to predict sentiment in sentences more accurately. Of course we haven't just stopped there. We've also categorized our sentiment analysis into different segments, according to market and industry. Someone that tweets something negative about music, could be seen as a neutral on another subject, creating false-positives. In addition, our team of analysis go over new words and phrases on a daily basis to add to our corpus. </p>
                                    <p>Each post content is classified into sentiment scores as follows: </p>
                                    <h4>-2 to -1 = Negative <br />
                                      -0.99 to -0.1 = Leaning Negative <br />
                                      0 = Neutral <br />
                                      +0.1 to +0.9 &nbsp;= Leaning Positive <br />
                                      +1 to +2 = Positive </h4><br />
                                    <p>And this is how we do it.&nbsp;<strong> </strong></p>
